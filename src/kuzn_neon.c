void kuzn_key_expansion(KuznCtx * ctx, const uint8_t* key, int klen, int ekb)
{
    uint8x16_t *K = ctx->K;
	uint8x16_t a, b;
    K[0] = a = REV128(vld1q_u8(key+ 0));//__builtin_memcpy(&K[0],&key[16], 16);
    K[1] = b = REV128(vld1q_u8(key+16));//__builtin_memcpy(&K[1],&key[ 0], 16);
//    K[0] = a = vld1q_u8(key+16);
//    K[1] = b = vld1q_u8(key+ 0);
    int j, n = 0;
    for(j=2; j<9; j+=2) {
        int i;
        for (i=0;i<8;i+=2){
            /* можно вычислять констранты налету
            Сx = L((v16qi){n+1,0...0})
            */
            //register uint8x16_t v = a;
            b = LS4(a ^ Cx[n++]) ^ b;
            a = LS4(b ^ Cx[n++]) ^ a;
            //b = v;
        }
        K[j] = a; K[j+1] = b;
    }
}

